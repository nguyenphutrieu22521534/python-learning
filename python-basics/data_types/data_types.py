# data_types.py

# This file showcases various data types in Python.

# Integer
integer_example = 10
print("Integer:", integer_example)

# Float
float_example = 10.5
print("Float:", float_example)

# String
string_example = "Hello, World!"
print("String:", string_example)

# Boolean
boolean_example_true = True
boolean_example_false = False
print("Boolean True:", boolean_example_true)
print("Boolean False:", boolean_example_false)

# List (as a reference for data types)
list_example = [1, 2, 3, 4, 5]
print("List:", list_example)

# Tuple (as a reference for data types)
tuple_example = (1, 2, 3)
print("Tuple:", tuple_example)

# Set (as a reference for data types)
set_example = {1, 2, 3}
print("Set:", set_example)

# Dictionary (as a reference for data types)
dict_example = {"key1": "value1", "key2": "value2"}
print("Dictionary:", dict_example)